/*
                    ******* Loops In JavaScript  *******

            Loops : used when need to repeat some task again & again.

              for-loop : for(initialization ; condition ; updation) {
                          //  block of code to repeat
                        }

              while loop : while(condition){
                                // block of code
                          }

              do-while loop : do{
                                // block of code
                              }
                              while(condition);



            # break and continue keywords :

            break : used to terminate the loop
            continue : used to skip the current iteration of loop and the control flow of the program goes to next iteration.
*/